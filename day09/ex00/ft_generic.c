#include <unistd.h>

void	ft_generic(void)
{
	char	*str;
	int		i;

	i = 0;
	str = "Tu tu tu tu ; Tu tu tu tu\n";
	while (str[i] != '\0')
	{
		write(1, &str[i], 1);
		i++;
	}
}
