void     ft_print_comb(void)
{  
    char i, j, k;
    i = '0'; j = '1'; k = '2';
  
    while(i<='7')
    {
      while(j<='8')
	{    
	  while(k<='9')
	    {
	      ft_putchar(i); ft_putchar(j); ft_putchar(k);
              k++;
	      if (i!='7') ft_putchar(','); ft_putchar(' ');
	    }
	  j++;
	  k=j+1;
	}     
      i++;
      j=i+1;
      k=j+1;
    }
  ft_putchar('\n');
}
